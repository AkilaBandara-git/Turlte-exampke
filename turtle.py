import turtle

my_turtle = turtle.Turtle()

def draw():
	my_turtle.forward(100)
	my_turtle.right(90)
	my_turtle.forward(100)
	my_turtle.right(90)
	my_turtle.forward(100)
	my_turtle.right(90)
	my_turtle.forward(100)

t = True
while t:
	draw()
	my_turtle.forward(150)
	draw()
	my_turtle.forward(150)
	draw()

